const dictionaries: Record<SupportedLanguage, PartialDictionary> = {
  en,
  ko,
  ja,
  ...extraDictionaries,
};

export function resolveLanguage(
  setting: LanguageSetting,
  systemLanguage = "en",
): SupportedLanguage {
  if (setting !== "auto") return setting;
  const normalized = systemLanguage.replace("_", "-").toLowerCase();
  if (normalized.startsWith("ko")) return "ko";
  if (normalized.startsWith("ja")) return "ja";
  if (normalized.startsWith("zh-tw") || normalized.startsWith("zh-hk") || normalized.startsWith("zh-mo")) return "zh-TW";
  if (normalized.startsWith("zh")) return "zh-CN";
  if (normalized.startsWith("pt")) return "pt-BR";
  for (const language of ["es", "fr", "de", "it", "nl", "pl", "ru", "uk", "tr", "ar", "hi", "id", "th", "vi"] as const) {
    if (normalized.startsWith(language)) return language;
  }
  return "en";
}

export function translate(
  setting: LanguageSetting,
  key: TranslationKey,
  values: Record<string, string | number> = {},
  systemLanguage = "en",
): string {
  const template = dictionaries[resolveLanguage(setting, systemLanguage)][key] ?? en[key];
  return template.replace(/\{(\w+)\}/g, (_, name: string) => String(values[name] ?? `{${name}}`));
}

export function isRtlLanguage(setting: LanguageSetting, systemLanguage = "en"): boolean {
  return resolveLanguage(setting, systemLanguage) === "ar";
}
